let numero1 = 10;
let numero2 = 100;

//operador de comparação ==

console.log(numero1==numero2);

//operador de comparação !=

console.log(numero1 != numero2);

//operador de comparação === // compara o valor e o tipo de dado

console.log(numero1 === numero2);

//operador de comparação > < =   <0, 1, 2, 3, 4, 5, 6>

console.log(numero1 > numero2);//false
console.log(numero1 >= numero2);//false
console.log(numero1 < numero2);//true
console.log(numero1 <= numero2);//true